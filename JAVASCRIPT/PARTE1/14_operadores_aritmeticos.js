/*      OPERADORES ARITMÉTICOS
        
        + -> Adição
        - -> Subtração
        * -> Multiplicação
        / -> Divisão
        % -> Resto
        ++ -> Incremento
        -- -> Decremento
        ** -> Exponencial       
 

*/

let number1 = 2
let number2 = 3


let calc = [number1+number2,number1-number2,number1*number2,number1/number2,number1%number2,
++number1,++number2,--number1,--number2,number1**number2 ]

console.log(calc)
console.log(calc.length)
