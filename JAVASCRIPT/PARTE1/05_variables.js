//VARIÁVEIS
// -> 3 modos de declarar variáveis:
// -> var
// -> let
// -> const

// - Permitido:

// - Iniciar com $ ou _
// - Colocar acentos
// - Iniciar com letras
// - Letras maiúsculas e minúsculas

// - Não pode:
// - Iniciar com números
// - Colocar espaços

// - Ideal:

// - Camel case (ex: "quantidadeDeMacasDoPote")
// - Snake Case (ex: "quantidade_de_macas_do_pote")
// - SEMPRE em inglês
// - Nomes que fazem sentido 

var number1 = 1 //Não usem o var.
let number2 = 2
const number3 = 3 // Constante, uma vez declarado, não pode mudar.

console.log(number1)
console.log(number2)
console.log(number3)